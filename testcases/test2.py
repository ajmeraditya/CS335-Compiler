def main():

    # Check precedence
    print("6 * 3 // 2 is: (6 * 3) // 2")
    print(6 * 3 // 2)
    print("18 // 3 * 2 is: (18 // 3) * 2")
    print(18 // 3 * 2)

    print("9 // 3 %% 2 is: (9 // 3) %% 2")
    print(9 // 3 % 2)
    print("9 %% 3 // 2 is: 9 %% (3 // 2)")
    print(9 % 3 // 2)

    print("6 / 3 * 2 is: (6 / 3) * 2")
    print(6 / 3 * 2)
    print("6 * 4 / 2 is: 6 * (4 / 2) ")
    print(6 * 4 / 2)

    print("6 * 3 + 2 is: (6 * 3) + 2")
    print(6 * 3 + 2)
    print("6 + 3 * 2 is: 6 + (3 * 2)")
    print(6 + 3 * 2)

    print("6 + 3 - 2 is: 6 + (3 - 2)")
    print(6 + 3 - 2)
    print("6 - 3 + 2 is: (6 - 3) + 2")
    print(6 - 3 + 2)

    # Check associativity
    print("\nChecking associativity:")
    print("2 * 3 * 2 is: 2 * (3 * 2) ")
    print(2 * 3 * 2)

    print("6 // 3 // 2 is: (6 // 3) // 2")
    print(6 // 3 // 2)

    print("8 %% 5 %% 3 is: (8 %% 5) %% 3 ")
    print(8 % 5 % 3)

    print("6 / 3 / 2 is: (6 / 3) / 2")
    print(6 / 3 / 2)

    print("6 * 3 * 2 is: (6 * 3) * 2")
    print(6 * 3 * 2)

    print("6 + 3 + 2 is: (6 + 3) + 2")
    print(6 + 3 + 2)

    print("6 - 3 - 2 is: (6 - 3) - 2")
    print(6 - 3 - 2)

    
if __name__ == "__main__":
    main()
